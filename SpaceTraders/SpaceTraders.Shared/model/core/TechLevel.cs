

/**
 * This enum represents the tech level of a given plant. The tech level
 * contributes to the prices of a good on a planet.
 *
 * @author Nick
 *
 */
public enum TechLevel {
    /**
     * Pre-agricultural level.
     */
    PRE_AG,
    /**
     * Agricultural level.
     */
    AGRICULTURE,
    /**
     * Medieval level.
     */
    MEDIEVAL,
    /**
     * Renaissance level.
     */
    RENAISSANCE,
    /**
     * Early-industrial level.
     */
    EARLY_INDUSTRIAL,
    /**
     * Industrial level.
     */
    INDUSTRIAL,
    /**
     * Post-industrial level.
     */
    POST_INDUSTRIAL,
    /**
     * Hi-tech level.
     */
    HI_TECH
}
